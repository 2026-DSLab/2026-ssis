def law_type_of(name: str) -> str:
    if name.endswith('시행규칙'):
        return '시행규칙'
    if name.endswith('시행령'):
        return '시행령'
    return '법률'


def main() -> int:
    configure_utf8_console()
    settings = load_settings()

    print("MySQL 연결 중 (utf8mb4 명시)...")
    conn = mysql.connector.connect(
        host=settings.db.host, port=settings.db.port,
        user=settings.db.user, password=settings.db.password,
        database=settings.db.database, charset="utf8mb4",
        use_unicode=True,
    )
    cur = conn.cursor()

    sql = """
        INSERT INTO watchlist (
            law_id, law_type, official_name, internal_name,
            dept_codes, status, last_serial_no, last_checked_at
        ) VALUES (%s, %s, %s, %s, %s, '현행', %s, NOW())
        ON DUPLICATE KEY UPDATE
            official_name = VALUES(official_name),
            internal_name = VALUES(internal_name),
            dept_codes = VALUES(dept_codes)
    """

    rows = []
    for name, law_id, serial in LAWS:
        official = RENAME.get(name, name)
        rows.append((law_id, law_type_of(official), official, name, None, serial))
    for name, rule_id, serial in ADMRULES:
        official = RENAME.get(name, name)
        rows.append((rule_id, '행정규칙', official, name, ADMRUL_DEPT.get(rule_id), serial))

    print(f"{len(rows)}건 적재 중...")
    cur.executemany(sql, rows)
    conn.commit()
    print(f"✅ {cur.rowcount}건 반영됨 (INSERT+UPDATE 합산이라 rowcount는 다를 수 있음)")

    # 검증 — 한글이 실제로 정상 저장됐는지 바로 확인
    cur.execute("SELECT COUNT(*) FROM watchlist")
    print(f"\nwatchlist 총 건수: {cur.fetchone()[0]}")

    cur.execute("SELECT law_id, official_name, internal_name FROM watchlist WHERE law_id IN ('009199','008113')")
    print("\n검증 (한글이 정상 출력되는지 육안 확인):")
    for row in cur.fetchall():
        print(f"  {row}")

    cur.close()
    conn.close()
    return 0


